worker_plot_pktdelay_queue () {
    PARAM_SESSION_ID="$1"
    shift
    PARAM_DN_TEST="$1"
    shift

    # the packets queue service time distribution
    FNDAT="tmp-pktdelay-queue-${PARAM_DN_TEST}.dat.gz"
